<h1 class='sobreh1'>Sobre o MINOS:</h1>

<p class="fic">
 A história do MINOS LABYRINTH se dá ínicio quando Dante, nosso protagonista e aspirante a historiador, se vê de frente a grande oportunidade de sua vida
 que é poder provar tanto a si mesmo quanto para as pessoas ao seu redor que criaturas mitólogicas existem; Durante uma de suas viagens em busca
 de mais informações Dante se depara com um velho maltrapilho que aos gritos dizia que os monstros existentes dentro de um labirinto fugiriam e destruiriam
 toda aquela cidade. Como se era esperado, ninguém deu ouvidos aos avisos daquele senhor lunático, apenas continuavam seguindo suas vidas e o ignorando, 
 algo dizia a Dante que aquela cena já havia se repetido inúmeras vezes. Já decidido a também ignorar aquele senhor o rapaz encontra caído no chão o que parecia
 ser um diário, o pequeno caderno estava sujo e mal conservado, mesmo com curiosidade o garoto decidiu devolver o objeto a aquele homem - "Pode ficar com essa
 porcaria, jogue fora! Faça o que quiser!" - foi o que Dante ouviu antes de ainda aos gritos o velho louco ir embora.
 Minos Labyrinth foi o nome que mais se repetiu a medida que ele o lia, descobrindo que o diário estava repleto de relatos de outros que também haviam se aventurado
 a entrar no labirinto, ficando surpreso ao perceber que o velho provavelmente fora o único que voltou com vida.
 Tomado pelo desejo de ver as criaturas com seus próprios olhos Dante decidiu que entraria naquele labirinto, seria a maior aventura de sua vida e o jovem não via a
hora de finalmente desvender o mistério que o "Minos Labyrinth" tinha a lhe ofercer.
</p>


